-- 11. RLS POLICIES
CREATE POLICY "Public SELECT divisions" ON divisions FOR SELECT USING (true);
CREATE POLICY "Public SELECT districts" ON districts FOR SELECT USING (true);
CREATE POLICY "Public SELECT upazilas" ON upazilas FOR SELECT USING (true);
CREATE POLICY "Public SELECT unions" ON unions FOR SELECT USING (true);
CREATE POLICY "Public SELECT villages" ON villages FOR SELECT USING (true);
CREATE POLICY "Public SELECT business_types" ON business_types FOR SELECT USING (true);

CREATE POLICY "Anyone can register" ON shareholders FOR INSERT WITH CHECK (true);
CREATE POLICY "User SELECT own" ON shareholders FOR SELECT USING (auth.uid() = auth_user_id);
CREATE POLICY "User UPDATE own" ON shareholders FOR UPDATE USING (auth.uid() = auth_user_id);
CREATE POLICY "Admin ALL shareholders" ON shareholders FOR ALL USING (
  EXISTS (SELECT 1 FROM admins WHERE auth_user_id = auth.uid())
);

CREATE POLICY "Admin SELECT self" ON admins FOR SELECT USING (auth.uid() = auth_user_id);
CREATE POLICY "Admin ALL admins" ON admins FOR ALL USING (
  EXISTS (SELECT 1 FROM admins WHERE auth_user_id = auth.uid())
);

CREATE POLICY "Participant can SELECT messages" ON messages FOR SELECT USING (
  auth.uid() = sender_id OR auth.uid() = receiver_id OR
  EXISTS (SELECT 1 FROM admins WHERE auth_user_id = auth.uid())
);
CREATE POLICY "Participant can INSERT messages" ON messages FOR INSERT WITH CHECK (
  auth.uid() = sender_id OR
  EXISTS (SELECT 1 FROM admins WHERE auth_user_id = auth.uid())
);
CREATE POLICY "Participant can UPDATE read status" ON messages FOR UPDATE USING (
  auth.uid() = receiver_id OR
  EXISTS (SELECT 1 FROM admins WHERE auth_user_id = auth.uid())
);

-- 12. STORAGE POLICIES
CREATE POLICY "Public read shareholder-photos" ON storage.objects
  FOR SELECT USING (bucket_id = 'shareholder-photos');
CREATE POLICY "Auth upload shareholder-photos" ON storage.objects
  FOR INSERT WITH CHECK (bucket_id = 'shareholder-photos' AND auth.role() = 'authenticated');

CREATE POLICY "Public read shareholder-nid" ON storage.objects
  FOR SELECT USING (bucket_id = 'shareholder-nid');
CREATE POLICY "Auth upload shareholder-nid" ON storage.objects
  FOR INSERT WITH CHECK (bucket_id = 'shareholder-nid' AND auth.role() = 'authenticated');

CREATE POLICY "Public read message-media" ON storage.objects
  FOR SELECT USING (bucket_id = 'message-media');
CREATE POLICY "Auth upload message-media" ON storage.objects
  FOR INSERT WITH CHECK (bucket_id = 'message-media' AND auth.role() = 'authenticated');

-- 12b. RPC FUNCTIONS (bypass RLS via SECURITY DEFINER)
CREATE OR REPLACE FUNCTION public.is_admin()
RETURNS BOOLEAN
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
  RETURN EXISTS (SELECT 1 FROM admins WHERE auth_user_id = auth.uid());
END;
$$;

CREATE OR REPLACE FUNCTION public.get_admin_auth_user_id()
RETURNS UUID
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  result UUID;
BEGIN
  SELECT auth_user_id INTO result FROM admins LIMIT 1;
  RETURN result;
END;
$$;

